#include <bits/stdc++.h>
using namespace std;

int main()
{
    int m, n;
    cin >> m >> n;
    for (int i = 0; i < m; i++)
    { // First loop
        cout << "i = " << i<< " for first loop" << endl;
    }
    for (int j = 0; j < n; j++)
    { // Second loop
        cout << "j = " << j<< " for second loop" << endl;
    }

    return 0;
}